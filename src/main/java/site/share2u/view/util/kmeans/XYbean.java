package site.share2u.view.util.kmeans;

public class XYbean {
	Integer X;
	Integer Y;
	
	public XYbean(Integer x, Integer y) {
		super();
		X = x;
		Y = y;
	}
	public Integer getX() {
		return X;
	}
	public void setX(Integer x) {
		X = x;
	}
	public Integer getY() {
		return Y;
	}
	public void setY(Integer y) {
		Y = y;
	}
	
}
